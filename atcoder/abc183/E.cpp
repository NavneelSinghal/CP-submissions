void solve(int _) {
    int h, w;
    cin >> h >> w;
    vector<string> a(h);
    for (auto &s : a) cin >> s;
    vector<mint> pre_j(w), pre_d(w + h - 1), pre_i(h);
    vector<vector<mint>> ans(h, vector<mint>(w, 0));
    ans[0][0] = 1;
    pre_i[0] = 0;
    pre_j[0] = 0;
    pre_d[w - 1] = 0;
    for (int i = 0; i < h; ++i) {
        for (int j = 0; j < w; ++j) {
            if (a[i][j] == '.') {
                ans[i][j] += pre_i[i] + pre_j[j] + pre_d[i - j + w - 1];
                pre_i[i] += ans[i][j];
                pre_j[j] += ans[i][j];
                pre_d[i - j + w - 1] += ans[i][j];
            } else {
                pre_i[i] = 0;
                pre_j[j] = 0;
                pre_d[i - j + w - 1] = 0;
            }
        }
    }
    cout << ans[h - 1][w - 1] << '\n';
    return;
}

signed main() {
    setIO();
    int t = 1;
    //cin >> t;
    for (int _t = 1; _t <= t; _t++) {
        //cout << "Case #" << _t << ": ";
        solve(_t);
    }
    return 0;
}

